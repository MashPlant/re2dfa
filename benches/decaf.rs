  pub struct Lexer<'a> {
    pub string: &'a [u8],
    pub cur_line: u32,
    pub cur_col: u32,
  }

  impl<'a> Lexer<'a> {
    pub fn new(string: &[u8]) -> Lexer {
      Lexer {
        string,
        cur_line: 1,
        cur_col: 1,
      }
    }

    pub fn next(&mut self) -> Option<Token<'a>> {
      loop {
        if self.string.is_empty() {
          return Some(Token { ty: TokenType::_Eof, piece: "".as_bytes(), line: self.cur_line, col: self.cur_col });
        }
        let mut max: Option<(&[u8], TokenType)> = None;
        for (re, ty) in LEX_RULES.iter() {
          match re.find(self.string) {
            None => {}
            Some(n) => {
              let n = n.as_bytes();
              if match max {
                None => true,
                Some((o, _)) => o.len() < n.len(),
              } { max = Some((n, *ty)); }
            }
          }
        }
        let (piece, ty) = max?;
        self.string = &self.string[piece.len()..];
        let (line, col) = (self.cur_line, self.cur_col);
        for (i, l) in piece.split(|&b| b == b'\n').enumerate() {
          if i == 0 {
            self.cur_col += l.len() as u32;
          } else {
            self.cur_line += 1;
            self.cur_col = l.len() as u32 + 1;
          }
        }
        if ty != TokenType::_Eps {
          break Some(Token { ty, piece, line, col });
        }
      }
    }
  }
